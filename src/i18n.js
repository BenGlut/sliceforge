export function makeT(lang) {
  return (key, params = {}) => {
    let s = dict[lang]?.[key] ?? dict.en[key] ?? key
    for (const [k, v] of Object.entries(params)) s = s.replaceAll(`{{${k}}}`, v)
    return s
  }
}
